#include <iostream> 

using namespace std; 

int main() {
    // Step 1: Start
    
    // Step 2: Print "Enter first number"
    cout << "Enter first number: ";
    
    // Step 3: Input number1
    int number1;
    cin >> number1;
    
    // Step 4: Print "Enter 2nd number"
    cout << "Enter second number: ";
    
    // Step 5: Input number2
    int number2;
    cin >> number2;
    
    // Step 6: Calculate sum
    int sum = number1 + number2;
    
    // Step 7: Print sum
    cout << "The sum is: " << sum << endl;
    
    // Step 8: Stop
    return 0; // Indicate the program ended successfully
}
